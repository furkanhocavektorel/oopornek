package service.Impl;

public class Islemler {


    //overloading
    public int toplama(int sayi1,int sayi2){
        return sayi1+sayi2;
    }
    public int cikartma(int sayi1,int sayi2){
        return sayi1-sayi2;
    }

    public double toplama(double sayi1,double sayi2){
        return sayi1+sayi2;
    }

    public double cikartma(double sayi1,double sayi2){
        return sayi1-sayi2;
    }

    public long toplama(long sayi1,long sayi2){
        return sayi1-sayi2;
    }

}
